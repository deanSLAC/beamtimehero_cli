"""beamtimehero — generic BL15-2 CLI.

Surfaces the tool catalog to humans and to LLM agents over claude-code's
Bash tool. Single binary, scope-segmented:

    beamtimehero ref         — bundled reference docs
    beamtimehero tool        — non-SPEC tools (data, logs, analysis, plots, file I/O)
    beamtimehero db          — action-log queries
    beamtimehero spec-read   — SPEC-bound reads (motor positions, beam status, scan #)
    beamtimehero spec-write  — SPEC-bound mutations. Every leaf requires --justification.

Discover via `--help` at any depth. `tool` / `spec-read` / `spec-write` /
`db` leaves are autogenerated from `tool_catalog.TOOL_DEFINITIONS`. `ref` is
hand-wired and pulls from `beamtimehero_cli.refdocs`.

Errors → JSON `{"ok": false, "error": "..."}` on stdout, non-zero exit.
Successful tool calls print the tool's stdout (image paths included) and exit 0.
"""
from __future__ import annotations

import argparse
import io
import json
import logging
import os
import sys
import time
import traceback
from pathlib import Path

from beamtimehero_cli import refdocs
from beamtimehero_cli.cli.profiles import PROFILES
from beamtimehero_cli.tool_catalog import TOOL_DEFINITIONS, execute_tool
from beamtimehero_cli.tool_catalog.categorize import CATEGORY_OVERRIDES, categorize


_CANONICAL_TREES = frozenset({
    "ref", "tool", "db", "spec-read", "spec-write",
    "spec-file", "s3df", "slack",
})


# ---------------------------------------------------------------------------
# JSON-schema → argparse mapping
# ---------------------------------------------------------------------------

_BOOL_TRUE = {"true", "yes", "on", "1"}
_BOOL_FALSE = {"false", "no", "off", "0"}


def _bool_from_str(s: str) -> bool:
    sl = s.strip().lower()
    if sl in _BOOL_TRUE:
        return True
    if sl in _BOOL_FALSE:
        return False
    raise argparse.ArgumentTypeError(f"expected boolean (true/false), got {s!r}")


def _json_arg(s: str):
    try:
        return json.loads(s)
    except json.JSONDecodeError as e:
        raise argparse.ArgumentTypeError(f"expected JSON, got {s!r}: {e}") from e


def add_arg(p: argparse.ArgumentParser, key: str, prop: dict, required: bool) -> None:
    flag = f"--{key.replace('_', '-')}"
    desc = (prop.get("description") or "").strip()
    kwargs: dict = {"help": desc}
    if "default" in prop:
        kwargs["default"] = prop["default"]
    if required:
        kwargs["required"] = True

    enum = prop.get("enum")
    if enum:
        kwargs["choices"] = [str(v) for v in enum]
        p.add_argument(flag, **kwargs)
        return

    t = prop.get("type", "string")
    if t == "integer":
        kwargs["type"] = int
        p.add_argument(flag, **kwargs)
    elif t == "number":
        kwargs["type"] = float
        p.add_argument(flag, **kwargs)
    elif t == "boolean":
        kwargs["type"] = _bool_from_str
        kwargs["metavar"] = "BOOL"
        p.add_argument(flag, **kwargs)
    elif t in ("array", "object"):
        kwargs["type"] = _json_arg
        kwargs["metavar"] = "JSON"
        p.add_argument(flag, **kwargs)
    else:
        p.add_argument(flag, **kwargs)


