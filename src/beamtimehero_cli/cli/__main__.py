# Build the parser
# ---------------------------------------------------------------------------


class ToolParser(argparse.ArgumentParser):
    def error(self, message: str) -> None:  # type: ignore[override]
        payload: dict = {"ok": False, "error": f"argparse: {message}"}
        print(json.dumps(payload))
        sys.exit(2)


def build_ref_subtree(subs: argparse._SubParsersAction) -> None:
    ref = subs.add_parser(
        "ref",
        help="Reference docs (use `ref --list` to enumerate, `ref <name>` to fetch).",
    )
    ref.add_argument("doc_name", nargs="?", help="Reference document name.")
    ref.add_argument(
        "--list", action="store_true", dest="list_docs",
        help="List all available reference documents.",
    )


_TREE_HELPS: dict[tuple[str, ...], str] = {
    ("tool",): "Non-SPEC tools: data queries, analysis, plotting, file I/O.",
    ("db",): "Action-log queries.",
    ("spec-read",): "SPEC-bound reads (motor positions, beam status). No mutation.",
    ("spec-write",): "SPEC-bound mutations. Every leaf requires --justification.",
    ("spec-file",): "Scan tools that read SPEC files directly (file-cache backend).",
    ("s3df",): "S3DF-deployment tools (Postgres metadata + pickle scan data).",
    ("s3df", "psql"): "Direct Postgres queries (raw SQL, command/log queries).",
    ("slack",): "Slack messaging tools.",
}


def build_catalog_subtrees(
    subs: argparse._SubParsersAction,
    tool_defs: list[dict],
) -> None:
    """Register every tool under its categorized tree (and sub-trees).

    Each tool's tree comes from ``categorize(tdef)`` and may be a multi-
    segment tuple for nested branches (e.g. ``("s3df", "psql")``).
    """
    # parent path → its add_subparsers() action
    bucket: dict[tuple[str, ...], argparse._SubParsersAction] = {}
    # path → the parser at that node (so we can attach further children)
    nodes: dict[tuple[str, ...], argparse.ArgumentParser] = {(): None}  # root sentinel

    def _ensure_bucket(path: tuple[str, ...]) -> argparse._SubParsersAction:
        if path in bucket:
            return bucket[path]
        parent = path[:-1]
        parent_subs = subs if parent == () else _ensure_bucket(parent)
        node = parent_subs.add_parser(
            path[-1], help=_TREE_HELPS.get(path, "").strip() or None,
        )
        nodes[path] = node
        bucket[path] = node.add_subparsers(
            dest=_dest_for(path), metavar="<command>",
        )
        return bucket[path]

    # Pre-create the known trees so they always appear in --help even if no
    # tools are registered there yet.
    for path in _TREE_HELPS:
        _ensure_bucket(path)

    for tdef in tool_defs:
        fn = tdef.get("function") or {}
        name = fn.get("name")
        if not name:
            continue
        category = categorize(tdef)
        params = fn.get("parameters") or {}
        properties = params.get("properties") or {}
        required = set(params.get("required") or [])
        description = (fn.get("description") or "").strip()

        cli_name = name.replace("_", "-")
        leaf = _ensure_bucket(category).add_parser(cli_name, help=description)
        leaf.set_defaults(_tool_name=name, _tool_category=category)
        for key, prop in properties.items():
            add_arg(leaf, key, prop or {}, key in required)


def _dest_for(path: tuple[str, ...]) -> str:
    """argparse dest for a tree's add_subparsers — `leaf` for top-level
    branches, `leaf_<parent>` for nested ones so they coexist on the
    same Namespace without clobbering each other.
    """
    return "leaf" if len(path) == 1 else f"leaf_{path[-2].replace('-', '_')}"


def _index_tool_defs(tool_defs: list[dict]) -> dict[tuple[str, ...], dict]:
    """Index tool defs by their canonical ``(tree, ..., name)`` path.

    Used by ``build_profile_subtrees`` to resolve aliases in O(1) and
    reach the JSON schema needed to clone arguments onto the profile leaf.
    """
    out: dict[tuple[str, ...], dict] = {}
    for tdef in tool_defs:
        fn = tdef.get("function") or {}
        name = fn.get("name")
        if not name:
            continue
        path = categorize(tdef) + (name,)
        out[path] = tdef
    return out


def build_profile_subtrees(
    subs: argparse._SubParsersAction,
    tool_defs: list[dict],
) -> None:
    """Register each agent profile as a top-level branch.

    A profile branch holds leaf parsers whose names are the profile's
    alias keys (kebab-case); each leaf is built from the canonical
    tool's JSON schema and dispatches to the same ``_tool_name`` /
    ``_tool_category`` the canonical leaf would set. The master catalog
    is untouched.
    """
    if not PROFILES:
        return

    index = _index_tool_defs(tool_defs)

    for profile_name, profile in PROFILES.items():
        # Guard against profile names colliding with canonical trees —
        # would shadow the canonical branch in --help.
        if profile_name in _CANONICAL_TREES:
            raise RuntimeError(
                f"Profile name {profile_name!r} collides with a canonical tree. "
                f"Rename the profile."
            )

        branch = subs.add_parser(
            profile_name,
            help=(profile.get("description") or "").strip() or None,
        )
        leaves = branch.add_subparsers(
            dest=_dest_for((profile_name,)), metavar="<command>",
        )

        for alias_name, canonical_path in (profile.get("aliases") or {}).items():
            canonical_path = tuple(canonical_path)
            tdef = index.get(canonical_path)
            if tdef is None:
                logging.getLogger(__name__).warning(
                    "Profile %r aliases %r -> %s but no such tool is registered; skipping.",
                    profile_name, alias_name, canonical_path,
                )
                continue

            fn = tdef.get("function") or {}
            description = (fn.get("description") or "").strip()
            params = fn.get("parameters") or {}
            properties = params.get("properties") or {}
            required = set(params.get("required") or [])

            leaf = leaves.add_parser(alias_name, help=description)
            canonical_tree = canonical_path[:-1]
            canonical_name = canonical_path[-1]
            leaf.set_defaults(
                _tool_name=canonical_name,
                _tool_category=canonical_tree,
            )
            for key, prop in properties.items():
                add_arg(leaf, key, prop or {}, key in required)


def build_parser() -> argparse.ArgumentParser:
    parser = ToolParser(
        prog="beamtimehero",
        description=(
            "Unified beamline CLI. Discover trees with `beamtimehero --help`; "
            "leaves with `beamtimehero <tree> <cmd> --help`."
        ),
    )
    parser.add_argument(
        "--list-profiles",
        action="store_true",
        dest="list_profiles",
        help="List registered agent profiles and their alias counts.",
    )
    trees = parser.add_subparsers(dest="tree", metavar="<tree>")

    build_ref_subtree(trees)
    build_catalog_subtrees(trees, TOOL_DEFINITIONS)
    build_profile_subtrees(trees, TOOL_DEFINITIONS)
    return parser


